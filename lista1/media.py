'''Escreva um programa que receba 3 inteiros e retorne a média aritmética deles.'''


# Entrada
n1 = int(input('Digite um número: '))
n2 = int(input('Digite um número: '))
n3 = int(input('Digite um número: '))

# Processamento
resultado = (n1 + n2 + n3)/3

# Saída
print('{} é a média aritmética desses números'.format(resultado))